print("El programa está orientado a calcular el tiempo de caída de un objeto que se suelta desde una altura h.")

t = float(input("Digite el tiempo en segundos que tardó el objeto en llegar de un punto de inicio al final: "))
v = float(input("Digite la velocidad en m/s que tuvo el objeto durante el recorrido: "))
print(f"La distancia en metros recorrida por el objeto en {t} segundos es de: {v * t} m")
