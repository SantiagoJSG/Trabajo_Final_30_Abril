# Ejercicio 4
print("El programa está orientado a sacar la suma, resta, multiplicación, división y residuo de los números ingresados.")
numero1 = float(input("Digita un número: "))
numero2 = float(input("Digita otro número: "))
suma = numero1 + numero2
resta = numero1 - numero2
multiplicacion = numero1 * numero2
division = numero1 / numero2
residuo = numero1 % numero2

print(f"La suma de los números ingresados es: {suma}")
print(f"La resta de los números ingresados es: {resta}")
print(f"La multiplicación de los números ingresados es: {multiplicacion}")
print(f"La división de los números ingresados es: {division}")
print(f"El residuo de los números ingresados es: {residuo}")
